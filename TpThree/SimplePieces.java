public class SimplePieces extends Piece{
    private double time;

    public SimplePieces(double t)
    {
        this.time = t;
    }

    public double fab_time()
    {
        return time;
    }

    public double get_time()
    {
        return time;
    }

    public void set_time(double t)
    {
        this.time = t;
    }
}
