public class Point {
    private float x, y;

    public Point(float X, float Y)
    {
        this.y = Y;
        this.x = X;
    }

    public float getx()
    {
        return this.x;
    }

    public void setx(float value)
    {
        this.x = value;
    }

    public float gety()
    {
        return this.y;
    }

    public void sety(float value)
    {
        this.y = value;
    }
}
